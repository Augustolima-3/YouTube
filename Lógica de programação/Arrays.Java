public class Aula01Arrays {

	public static void main(String[] args) {

		double[] notas = new double [4];
		notas[0] = 6;
		notas[1] = 2;
		notas[2] = 10;
		notas[3] = 10;
		
		System.out.println("Tamanho do array: "+notas.length);
		
		for(int i = 0; i < notas.length; i ++) {
			
		System.out.println(notas[i]);
		}
		
				
		
	}

}
